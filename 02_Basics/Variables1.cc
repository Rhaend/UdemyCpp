#include <iostream>

// 1.) Can not Start with a Number
// 2.) Can not contain whitespaces
// 3.) Variable name must be unique

int main()
{
    int my_integer_value = 2;
    float my_float_value = 3.5;
    std::cout << "Value1: " << my_integer_value << std::endl;
    std::cout << "Value2: " << my_float_value << std::endl;

    return 0;
}
